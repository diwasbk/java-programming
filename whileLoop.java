public class whileLoop {
    
    public static void main(String[] args) {
        int i = 1;  // Initialize variable i to 1
        
        // Start while loop that continues while i is less than or equal to 10
        while(i <= 10){
            System.out.println(i);  // Print the current value of i
            i++;  // Increment i by 1
        }
    }
}